import UIKit

extension UIColor {
    
    static func bounceBlack() -> UIColor {
        return UIColor(red:0.20000, green:0.20000, blue:0.20000, alpha:1.00000)
    }
    
    static func dribbblePink() -> UIColor {
        return UIColor(red:0.85490, green:0.34902, blue:0.53333, alpha:1.00000)
    }
    
}
